# 演示type（）使用
age = 18
name = "林黛玉"
boring = True
score = 99.5

print(type(age))
print(type(name))
print(type(boring))
print(type(score))
print(f"年纪:{age}，姓名:{name}，判断:{boring}，得分:{score}")
print(f"年纪: {type(age)}，姓名: {type(name)}，判断: {type(boring)}，得分: {type(score)}")

